//Comparision Operators 
const a = 10;
const b = 5;

console.log(a >= b); 
console.log(a <= b); 
console.log(a != b); 
console.log(a === b); // Strict equality
console.log(a !== b); // Strict Inequality


// Strict vs loose 

console.log(0 == '0'); //true
console.log(0 === '0'); //false
console.log(true == "true"); //false
console.log(true === "true");//false